def power(base, exponent):
    """Вычисляет степень числа."""
    return base ** exponent


def square_root(value):
    """Вычисляет квадратный корень."""
    return value ** 0.5


def area_of_circle(radius):
    """Вычисляет площадь круга."""
    return 3.1416 * radius ** 2
